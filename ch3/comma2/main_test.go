package main

import "testing"

func TestComma(t *testing.T) {
	var tests = []struct {
		input, want string
	}{
		{"1", "1"},
		{"12", "12"},
		{"123", "123"},
		{"1234", "1,234"},
		{"12345", "12,345"},
		{"123456", "123,456"},
		{"1234567", "1,234,567"},
		{"12345678", "12,345,678"},
		{"123456789", "123,456,789"},
		{"1234567890", "1,234,567,890"},
		{"-1", "-1"},
		{"-12", "-12"},
		{"-123", "-123"},
		{"-1234", "-1,234"},
		{"-12345", "-12,345"},
		{"-123456", "-123,456"},
		{"-1234567", "-1,234,567"},
		{"-12345678", "-12,345,678"},
		{"-123456789", "-123,456,789"},
		{"-1234567890", "-1,234,567,890"},
		{"1.123", "1.123"},
		{"12.123", "12.123"},
		{"123.123", "123.123"},
		{"1234.123", "1,234.123"},
		{"12345.123", "12,345.123"},
		{"123456.123", "123,456.123"},
		{"1234567.123", "1,234,567.123"},
		{"12345678.123", "12,345,678.123"},
		{"123456789.123", "123,456,789.123"},
		{"1234567890.123", "1,234,567,890.123"},
		{"-1.123", "-1.123"},
		{"-12.123", "-12.123"},
		{"-123.123", "-123.123"},
		{"-1234.123", "-1,234.123"},
		{"-12345.123", "-12,345.123"},
		{"-123456.123", "-123,456.123"},
		{"-1234567.123", "-1,234,567.123"},
		{"-12345678.123", "-12,345,678.123"},
		{"-123456789.123", "-123,456,789.123"},
		{"-1234567890.123", "-1,234,567,890.123"},
	}

	for _, test := range tests {
		if got := comma(test.input); got != test.want {
			t.Errorf("comma(%q), got %q, want %q", test.input, got, test.want)
		}
	}
}
